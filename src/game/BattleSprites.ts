function cloneRows(rows: SpriteRows): SpriteRows {
  return rows.map((row) => row.slice());
}

function setPixel(rows: SpriteRows, x: number, y: number, value: string) {
  if (!rows[y] || x < 0 || x >= rows[y].length) return;
  rows[y] = `${rows[y].slice(0, x)}${value}${rows[y].slice(x + 1)}`;
}

function hashString(input: string): number {
  let hash = 2166136261;
  for (let i = 0; i < input.length; i++) {
    hash ^= input.charCodeAt(i);
    hash = Math.imul(hash, 16777619);
  }
  return hash >>> 0;
}

function mutateRows(rows: SpriteRows, seed: number): SpriteRows {
  const next = cloneRows(rows);
  if (seed & 1) {
    setPixel(next, 5, 4, '2');
    setPixel(next, 10, 4, '2');
  }
  if (seed & 2) {
    setPixel(next, 7, 9, '3');
    setPixel(next, 8, 9, '3');
  }
  if (seed & 4) {
    setPixel(next, 4, 11, '3');
    setPixel(next, 11, 11, '3');
  }
  return next;
}

function shiftPalette(base: Palette, seed: number): Palette {
  if (seed % 3 === 0) return base;
  if (seed % 3 === 1) {
    return [base[0], base[1], base[2], base[3]];
  }
  return [base[0], base[1], base[2], base[3]];
}

export function getBattleCreatureVisual(name: string, type: string, seedSource?: string): BattleVisual {
  const rows = BATTLE_SPRITE_ROWS[name] || BATTLE_SPRITE_ROWS.player;
  const seed = hashString(`${seedSource || name}:${type}`);
  const palette = shiftPalette(TYPE_PALETTES[type] || PAL.LEYLINE, seed);
  return {
    sprite: buildSprite(mutateRows(rows, seed)),
    palette,
  };
}

export function getBattlePlayerVisual(): BattleVisual {
  return {
    sprite: buildSprite(BATTLE_SPRITE_ROWS.player),
    palette: PAL.PLAYER,
  };
}
